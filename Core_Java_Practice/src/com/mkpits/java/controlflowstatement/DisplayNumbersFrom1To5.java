package src.com.mkpits.java.controlflowstatement;
//Java Program to example of Display numbers from 1 to 5.

class DisplayNumbersFrom1To5 {
    public static void main(String[] args) {

        int n = 5;
        // for loop
        for (int i = 1; i <= n; ++i) {
            System.out.println(i);
        }
    }
}