package org.mkpits.java.whileloopdowhileloop;
//Write a program to print even numbers between 1 and 10.

import java.util.*;
public class EvenNumbersBetween1And10 {
    public static void main(String[] arg)
    {
        int i;
        for(i=0; i<=10 ; i=i+2)
        {
            System.out.println(i); // 0 2 4
        }
    }
}