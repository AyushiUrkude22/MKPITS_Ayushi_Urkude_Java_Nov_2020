//Write a program to print no. from 1 to 10 using for loop.

import java.util.*;
class PrintNumberFrom1to10UsingForLoop {
    public static void main(String[] arg)
    {
        int i;
        for(i=1; i<=10 ; i++)
        {
            System.out.println(i);
        }
    }
}
